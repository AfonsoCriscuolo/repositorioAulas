package exemplos;

public class Exemplo02 {
    
    public static void main(String[] args) {
        int numero; // declaração / criação de variável

        numero = 10;  // atribuição - guardar o valor da variável

        System.out.println(numero);

        numero = 5; //variável armazena apenas um valor de cada vez

        System.out.println(numero);

        numero = 7 * 2;
        System.out.println(numero);

        numero = numero + 1;

        System.out.println(numero);

    }
}
